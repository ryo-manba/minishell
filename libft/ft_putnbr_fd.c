#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	num;

	num = (long)n;
	if (num < 0)
	{
		ft_putchar_fd('-', fd);
		num = -num;
	}
	if (num >= 10)
	{
		ft_putnbr_fd(num / 10, fd);
		ft_putnbr_fd(num % 10, fd);
	}
	else
		ft_putchar_fd(num + '0', fd);
}

void	ft_putsizet_fd(size_t n, int fd)
{
	if (n / 10)
		ft_putnbr_fd(n / 10, fd);
	write(fd, &("0123456789"[n % 10]), 1);
}
